class ImageSwapper extends StatefulWidget {
  const ImageSwapper({super.key});

  @override
  _ImageSwapperState createState() => _ImageSwapperState();
}

class _ImageSwapperState extends State<ImageSwapper> {
  List<Map<String, String>> imageDetails = [
    {
      'url': 'assets/homeremedies/Coriander Seed.jpg',
      'details':
          'Coriander seeds have analgesic properties that can help relieve headache pain. Boil a teaspoon of coriander seeds in water for about 10-15 minutes. Strain and drink the decoction warm. ',
      'name': 'Coriander Seed Decoction'
    },
    {
      'url': 'assets/homeremedies/Honey and Lemon.jpg',
      'details':
          ' Lemon and honey have soothing properties that can help alleviate headaches. Mix the juice of half a lemon with a tablespoon of honey in warm water and drink it slowly. ',
      'name': 'Lemon and Honey Decoction'
    },
    {
      'url': 'assets/homeremedies/Cinnamon and Clove.jpg',
      'details':
          'Cinnamon and cloves have anti-inflammatory properties that can help reduce headache pain. Boil a few cinnamon sticks and cloves in water for about 10-15 minutes. Strain and drink the decoction warm. ',
      'name': 'Cinnamon and Clove Decoction'
    },
    {
      'url': 'assets/homeremedies/Brahmi.jpg',
      'details':
          'Brahmi is an herb known for its calming and stress relieving properties. Boil Brahmi leaves in water for about 10-15 minutes. Strain and drink the decoction warm to help alleviate tension headaches.',
      'name': 'Brahmi (Gotu Kola) Decoction'
    },
  ];

  int _selectedImageIndex = -1;

  void _toggleDetails(int index) {
    setState(() {
      if (_selectedImageIndex == index) {
        _selectedImageIndex = -1;
      } else {
        _selectedImageIndex = index;
      }
    });
  }

  @override
  Widget build(BuildContext context) {
    return ListView.builder(
      shrinkWrap: true,
      physics: const NeverScrollableScrollPhysics(),
      itemCount: imageDetails.length,
      itemBuilder: (context, index) {
        return Column(
          children: [
            Padding(
              padding: const EdgeInsets.all(8.0),
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.center,
                children: [
                  Text(
                    imageDetails[index]['name']!,
                    style: const TextStyle(
                      fontSize: 16,
                      fontWeight: FontWeight.bold,
                    ),
                    textAlign: TextAlign.center,
                  ),
                  InkWell(
                    onTap: () {
                      _toggleDetails(index);
                    },
                    child: Image.asset(
                      imageDetails[index]['url']!,
                      fit: BoxFit.cover,
                    ),
                  ),
                ],
              ),
            ),
            if (_selectedImageIndex == index)
              Padding(
                padding: const EdgeInsets.symmetric(horizontal: 8.0),
                child: Container(
                  decoration: BoxDecoration(
                    color: Colors.white,
                    border: Border.all(color: Colors.grey),
                    borderRadius: BorderRadius.circular(8),
                    boxShadow: [
                      BoxShadow(
                        color: Colors.grey.withOpacity(0.5),
                        spreadRadius: 2,
                        blurRadius: 5,
                        offset: const Offset(0, 3),
                      ),
                    ],
                  ),
                  padding: const EdgeInsets.all(8.0),
                  child: Text(
                    imageDetails[index]['details']!,
                    style: const TextStyle(fontSize: 14),
                    textAlign: TextAlign.justify,
                  ),
                ),
              ),
            const SizedBox(height: 30), // Added more space between pictures
          ],
        );
      },
    );
  }
}